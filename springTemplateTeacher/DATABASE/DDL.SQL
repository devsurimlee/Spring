CREATE TABLE JOB_NOTICE (
NOTICE_DATE		VARCHAR2(8),
NOTICE_IDEN		VARCHAR2(5),
NOTICE_STATE	VARCHAR2(1000),
PERSON			NUMBER(5),
QUALI_FIELD		VARCHAR2(3),
LANGUAGE_CODE	VARCHAR2(5),
CONTENTS		VARCHAR2(4000),
MANAGER			VARCHAR2(50),
APPLY_DATE		DATE,
MAGAN_DATE		DATE,
PRIMARY KEY (NOTICE_IDEN)    
)